def load_text(path: str | None) -> str:
    if path is None:
        return EXAMPLE_TEXT
    p = Path(path)
    if not p.exists():
        print(f"文件 {path} 不存在，使用默认示例。")
        return EXAMPLE_TEXT
    return p.read_text(encoding="utf-8")

@profile
def main():
    src_path = sys.argv[1] if len(sys.argv) > 1 else None
    article = load_text(src_path)
    print("原文:\n", article)
